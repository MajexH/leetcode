package RemoveDuplicatesfromSortedList82and83;

public class ListNode {

    public int val;
    public ListNode next;

    public ListNode() {};

    public ListNode(int val) {
        this.val = val;
        this.next = null;
    }

    @Override
    public String toString() {
        return "ListNode{" +
                "val=" + val +
                ", next=" + next +
                '}';
    }
}