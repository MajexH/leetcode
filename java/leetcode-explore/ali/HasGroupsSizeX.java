    // 选定一个数字 X 问能否形成 X 个为一组的情况 使得每一个分组里面的数字相同
    public boolean hasGroupsSizeX(int[] deck) {
        // x >= 2
        if (deck == null || deck.length < 2) return false;

        Map<Integer, Integer> map = new HashMap<>();
        for (int num : deck) {
            map.put(num, map.getOrDefault(num, 0) + 1);
        }
        // 只有一个数字 肯定是可以的
        if (map.size() == 1) return true;

        // 其实是找 数量是否有最小的公因子
        int pre = 0;
        List<Integer> nums = new ArrayList<>(map.values());
        for (int i = 1; i < nums.size(); i++) {
            if (i == 1) {
                // 用 pre 来代表强两个数的最小公因子
                pre = getGCD(nums.get(0), nums.get(1));
            } else {
                // 因为相等的数 求 GCP 是自己 所以只要能够被 pre 整除也行
                int gcd = getGCD(nums.get(i - 1), nums.get(i));
                // 更新 pre 取 当前 gcd 与 之前 pre gcd 的最大公因子，不停的提取最后的公因子 判断是否 >1(题目要求)
                pre = getGCD(gcd, pre);
            }
            // 最小公因子不能为 1 k >= 2
            if (pre == 1) return false;
        }
        return true;
    }

    public int getGCD(int a, int b) {
        while (b != 0) {
            int tmp = a;
            a = b;
            b = tmp % b;
        }
        return a;
    }

